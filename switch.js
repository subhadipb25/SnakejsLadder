var prompt = require('prompt-sync')();
var weekNo = Number(prompt("Enter Any Number:"));
switch(weekNo){
    case 1:
        console.log("Monday");
        break;
        case 2:
            console.log("Tuesday");
            break;
            case 3:
                console.log("Wednesday");
                break;
                case 4:
                    console.log("Thursday");
                    break;
                    case 5:
                        console.log("Friday");
                        break;
                        case 6:
                            console.log("Satarday");
                            break;
                            case 7:
                                console.log("wrong number");
                                
}